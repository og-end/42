int	ft_count_if(char **tab, int (*f)(char*))
{
	int	c;
	int	i;

	if (!tab)
		return (0);
	c = 0;
	i = -1;
	while (tab[++i])
	{
		if (f(tab[i]))
			c++;
	}
	return (c);
}
